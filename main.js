function ProjOne() {
    var x = document.getElementById("divone");
    var c = document.getElementById("onebuzz");
    if (x.style.display !== "flex") {
        x.style.display = "flex";
        c.style.color = "var(--main)";
    } else {
        x.style.display = "none";
        c.style.color = "rgba(100, 100, 100, 0.6)";
    }
}

